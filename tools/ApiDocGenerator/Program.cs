enum MemberKind { Type, Method, Property, Field, Event }

record DocMember(string RawName, string Assembly, XElement Element)
{
    public MemberKind Kind => RawName[0] switch
    {
        'T' => MemberKind.Type,
        'M' => MemberKind.Method,
        'P' => MemberKind.Property,
        'F' => MemberKind.Field,
        'E' => MemberKind.Event,
        _ => MemberKind.Type,
    };

    public string FullName => RawName.Length > 2 ? RawName[2..] : RawName;

    public string FullTypeName
    {
        get
        {
            if (Kind == MemberKind.Type) return FullName;
            var name = FullName;
            var parenIdx = name.IndexOf('(');
            if (parenIdx >= 0) name = name[..parenIdx];
            var lastDot = name.LastIndexOf('.');
            return lastDot >= 0 ? name[..lastDot] : name;
        }
    }

    public string Namespace
    {
        get
        {
            var typeName = Kind == MemberKind.Type ? FullName : FullTypeName;
            var clean = typeName;
            var btIdx = clean.IndexOf('`');
            if (btIdx >= 0) clean = clean[..btIdx];
            var lastDot = clean.LastIndexOf('.');
            return lastDot >= 0 ? clean[..lastDot] : "";
        }
    }

    public string TypeName
    {
        get
        {
            var typeName = Kind == MemberKind.Type ? FullName : FullTypeName;
            var lastDot = typeName.LastIndexOf('.');
            var name = lastDot >= 0 ? typeName[(lastDot + 1)..] : typeName;
            return CleanGeneric(name);
        }
    }

    public string DisplayTypeName => TypeName;

    public string MemberName
    {
        get
        {
            if (Kind == MemberKind.Type) return TypeName;
            var name = FullName;
            var parenIdx = name.IndexOf('(');
            if (parenIdx >= 0) name = name[..parenIdx];
            var lastDot = name.LastIndexOf('.');
            return lastDot >= 0 ? CleanGeneric(name[(lastDot + 1)..]) : CleanGeneric(name);
        }
    }

    public string DisplaySignature
    {
        get
        {
            if (Kind == MemberKind.Property)
                return $"{MemberName} {{ get; }}";

            if (Kind == MemberKind.Method)
            {
                var paramElements = Element.Elements("param").ToList();
                var paramList = string.Join(", ", paramElements.Select(p => p.Attribute("name")?.Value ?? "?"));
                return $"{MemberName}({paramList})";
            }

            return MemberName;
        }
    }

    private static string CleanGeneric(string name)
    {
        var idx = name.IndexOf('`');
        if (idx >= 0) return $"{name[..idx]}<T>";
        return name;
    }
}
