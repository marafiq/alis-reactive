static string FindRepoRoot()
{
    var dir = Directory.GetCurrentDirectory();
    while (dir != null)
    {
        if (File.Exists(Path.Combine(dir, "package.json")) &&
            Directory.Exists(Path.Combine(dir, "Alis.Reactive")))
            return dir;
        dir = Directory.GetParent(dir)?.FullName;
    }
    return Directory.GetCurrentDirectory();
}

static bool IsInternal(DocMember m)
{
    var summary = m.Element.Element("summary")?.Value ?? "";
    if (summary.Contains("NEVER make public", StringComparison.OrdinalIgnoreCase))
        return true;
    if (m.Kind == MemberKind.Method && m.MemberName == "#ctor")
        return true;
    return false;
}

static string GetSummaryText(XElement member)
{
    var summary = member.Element("summary");
    if (summary == null) return "";
    return CleanXmlText(summary);
}

static string GetSummaryInline(XElement member)
{
    var summary = member.Element("summary");
    if (summary == null) return "";
    return CleanXmlText(summary).Trim().Replace("\n", " ");
}

static string CleanXmlText(XElement element)
{
    var sb = new StringBuilder();
    foreach (var node in element.Nodes())
    {
        if (node is XText text)
        {
            sb.Append(text.Value);
        }
        else if (node is XElement el)
        {
            switch (el.Name.LocalName)
            {
                case "c":
                    sb.Append($"`{el.Value}`");
                    break;
                case "code":
                    sb.AppendLine();
                    sb.AppendLine("```csharp");
                    sb.AppendLine(el.Value.Trim());
                    sb.AppendLine("```");
                    break;
                case "see":
                    var cref = el.Attribute("cref")?.Value;
                    var langword = el.Attribute("langword")?.Value;
                    if (langword != null)
                        sb.Append($"`{langword}`");
                    else if (cref != null)
                        sb.Append($"`{SimplifyCref(cref)}`");
                    else
                        sb.Append(el.Value);
                    break;
                case "para":
                    sb.AppendLine();
                    sb.Append(CleanXmlText(el));
                    break;
                case "paramref":
                    sb.Append($"`{el.Attribute("name")?.Value}`");
                    break;
                case "typeparamref":
                    sb.Append($"`{el.Attribute("name")?.Value}`");
                    break;
                case "b":
                    sb.Append($"**{el.Value}**");
                    break;
                default:
                    sb.Append(CleanXmlText(el));
                    break;
            }
        }
    }
    return sb.ToString();
}

static string SimplifyCref(string cref)
{
    if (cref.Length > 2 && cref[1] == ':')
        cref = cref[2..];
    var lastDot = cref.LastIndexOf('.');
    if (lastDot >= 0) cref = cref[(lastDot + 1)..];
    return CleanGenericNotation(cref);
}

static string SimplifyNamespace(string ns)
{
    return ns.Replace("Alis.Reactive.", "").Replace(".", " — ");
}

static string CleanGenericNotation(string name)
{
    var idx = name.IndexOf('`');
    if (idx >= 0) return $"{name[..idx]}<T>";
    return name;
}

